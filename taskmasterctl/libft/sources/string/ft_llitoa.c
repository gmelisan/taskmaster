#include "libft.h"

char	*ft_llitoa(unsigned long long int nb)
{
	int		i;
	int		j;
	char	tmp[64];
	char	*str;

	j = 0;
	while (nb)
	{
		tmp[j] = nb % 10 + '0';
		nb /= 10;
		++j;
	}
	str = (char*)ft_xmalloc(sizeof(char) * (j + 1));
	i = 0;
	while (j > 0)
	{
		str[i] = tmp[j - 1];
		i++;
		j--;
	}
	return (str);
}
